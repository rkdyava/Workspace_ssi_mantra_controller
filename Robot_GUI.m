% --- Executes on button press in btn_forward.
function btn_forward_Callback(hObject, eventdata, handles)
% hObject    handle to btn_forward (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
for Th_1=[0:0.1:str2double(handles.Theta_1.String)*pi/180];
% Th_1=str2double(handles.Theta_1.String)*pi/180;
Th_2=str2double(handles.Theta_2.String)*pi/180;
Th_3=str2double(handles.Theta_3.String)*pi/180;
Th_4=str2double(handles.Theta_4.String)*pi/180;
Th_5=str2double(handles.Theta_5.String)*pi/180;
Th_6=str2double(handles.Theta_6.String)*pi/180;
Th_7=str2double(handles.Theta_7.String)*pi/180;
%%
L_1=20;
L_2=10;
L_3=10;
L_4=10;
L_5=10;
L_6=10;
L_7=10;
L(1)=Link([0 L_1 0 pi/2]);
L(2)=Link([0 L_2 0 pi/2]);
L(3)=Link([0 L_3 0 pi/2]);
L(4)=Link([0 L_4 0 pi/2]);
L(5)=Link([0 L_5 0 pi/2]);
L(6)=Link([0 L_6 0 pi/2]);
L(7)=Link([0 L_7 0 pi/2]);
Robot=SerialLink(L);
Robot.name='Robot_GUI';
%%
Robot.plot([Th_1 Th_2 Th_3 Th_4 Th_5 Th_6 Th_7]);

T=Robot.fkine([Th_1 Th_2 Th_3 Th_4 Th_5 Th_6 Th_7])
 
handles.P_x.String=num2str(floor(T(1,4)));
handles.P_y.String=num2str(floor(T(2,4)));
handles.P_z.String=num2str(floor(T(3,4)));
% pause(0.1)
end

